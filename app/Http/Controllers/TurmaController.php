    {
       $turma=new Turma;
       $turma->descricao=$request->descricao;
       $turma->anoLectivo_id=$request->anoLectivo;
       $turma->criador_id=$request->criador;

       $turma->save();
       Alert::success('sucesso', 'Turma criada com sucesso');
       return back();
    }

